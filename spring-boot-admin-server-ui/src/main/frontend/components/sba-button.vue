<template>
  <button @click="$emit('click', $event)"
          :disabled="disabled"
          class="btn relative"
          :class="classNames"
  >
    <slot />
  </button>
</template>

<script>
export default {
  name: 'SbaButton',
  props: {
    flat: {
      type: Boolean,
      default: false
    },
    size: {
      type: String,
      default: 'sm'
    },
    disabled: {
      type: Boolean,
      default: false
    },
    buttonClass: {
      type: Object,
      default: () => {}
    }
  },
  computed: {
    classNames() {
      return {
        ...this.buttonClass,
        'rounded-md shadow-sm': !this.flat,
        // Sizes
        'px-3 py-2': this.size === 'sm',
        'px-5 py-2.5': this.size === ''
        // Types
      };
    }
  }
}
</script>

